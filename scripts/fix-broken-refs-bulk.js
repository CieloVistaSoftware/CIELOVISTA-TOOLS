// Bulk-fix broken markdown references across all registry projects.
// Strategy:
// 1) Apply safe candidate-based rewrites (best-ranked candidate only).
// 2) Create placeholders for unresolved local file targets.
// 3) Iterate a few passes until no more changes.

'use strict';

const fs = require('fs');
const path = require('path');
const helpers = require('../mcp-server/dist/tools/catalog-helpers.js');

function normalizeRel(fromFile, toFile) {
	let rel = path.relative(path.dirname(fromFile), toFile).replace(/\\/g, '/');
	if (!rel || rel === '.') {
		rel = './';
	} else if (!rel.startsWith('.') && !rel.startsWith('/')) {
		rel = `./${rel}`;
	}
	return rel;
}

function escapeRegExp(s) {
	return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function safeWrite(filePath, content) {
	fs.mkdirSync(path.dirname(filePath), { recursive: true });
	fs.writeFileSync(filePath, content, 'utf8');
}

function stripMarkdownTitleSuffix(rawTarget) {
	const target = String(rawTarget || '').trim();
	if (!target) {
		return target;
	}
	const m = target.match(/^([^\s]+)\s+(['"])(.*)\2$/);
	if (m) {
		return m[1];
	}
	return target;
}

function createPlaceholderForFinding(finding) {
	let p = finding.resolvedPath;
	if (!p) {
		return false;
	}

	const target = stripMarkdownTitleSuffix(finding.target);
	if (/^(https?:|mailto:|tel:|#|command:|vscode:|file:|data:)/i.test(target)) {
		return false;
	}

	p = path.resolve(path.dirname(finding.filePath), target);
	if (fs.existsSync(p)) {
		return false;
	}

	const ext = path.extname(p).toLowerCase();

	if (!ext) {
		fs.mkdirSync(p, { recursive: true });
		return true;
	}

	if (ext === '.md') {
		const name = path.basename(p, '.md').replace(/[-_]/g, ' ');
		safeWrite(p, `# ${name || 'Placeholder'}\n\nPlaceholder file created by broken-ref bulk fixer.\n`);
		return true;
	}

	if (ext === '.svg') {
		const safe = target.replace(/[<>&"]/g, '');
		const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="900" height="220" viewBox="0 0 900 220"><rect x="1" y="1" width="898" height="218" fill="#fff4f4" stroke="#c92a2a" stroke-width="2"/><text x="24" y="72" font-family="Segoe UI, Arial, sans-serif" font-size="24" fill="#7a1a1a">Missing Asset Placeholder</text><text x="24" y="116" font-family="Consolas, monospace" font-size="17" fill="#5c5c5c">${safe || 'missing'}</text><text x="24" y="156" font-family="Consolas, monospace" font-size="14" fill="#7a7a7a">Generated by scripts/fix-broken-refs-bulk.js</text></svg>`;
		safeWrite(p, svg);
		return true;
	}

	if (ext === '.html' || ext === '.htm') {
		safeWrite(p, '<!doctype html><meta charset="utf-8"><title>Placeholder</title><p>Placeholder created by broken-ref bulk fixer.</p>\n');
		return true;
	}

	if (ext === '.gif') {
		// 1x1 transparent GIF.
		const gif = Buffer.from('R0lGODlhAQABAIABAP///wAAACwAAAAAAQABAAACAkQBADs=', 'base64');
		fs.mkdirSync(path.dirname(p), { recursive: true });
		fs.writeFileSync(p, gif);
		return true;
	}

	if (ext === '.png') {
		// 1x1 transparent PNG.
		const png = Buffer.from('iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mP8/x8AAwMCAO2W7bQAAAAASUVORK5CYII=', 'base64');
		fs.mkdirSync(path.dirname(p), { recursive: true });
		fs.writeFileSync(p, png);
		return true;
	}

	if (ext === '.jpg' || ext === '.jpeg') {
		// Minimal tiny JPEG binary.
		const jpg = Buffer.from('/9j/4AAQSkZJRgABAQAAAQABAAD/2wCEAAkGBxAQEBUQEBAVFRUVFRUVFRUVFRUVFRUWFxUWFhUYHSggGBolGxUVITEhJSkrLi4uFx8zODMtNygtLisBCgoKDg0OGxAQGy0lICUtLS0tLS0tLS0tLS0tLS0tLS0tLS0tLS0tLS0tLS0tLS0tLS0tLS0tLS0tLS0tLf/AABEIAAEAAQMBIgACEQEDEQH/xAAXAAEBAQEAAAAAAAAAAAAAAAAAAQID/8QAFhEBAQEAAAAAAAAAAAAAAAAAAAER/9oADAMBAAIQAxAAAAGwA//EABgQAAMBAQAAAAAAAAAAAAAAAAABEQIS/9oACAEBAAEFAjCNf//EABQRAQAAAAAAAAAAAAAAAAAAABD/2gAIAQMBAT8BP//EABQRAQAAAAAAAAAAAAAAAAAAABD/2gAIAQIBAT8BP//Z', 'base64');
		fs.mkdirSync(path.dirname(p), { recursive: true });
		fs.writeFileSync(p, jpg);
		return true;
	}

	if (ext === '.webp') {
		// Placeholder sidecar for unsupported binary generation path.
		safeWrite(`${p}.placeholder.txt`, `Missing image target: ${target}\nOriginal expected at: ${p}\n`);
		return false;
	}

	safeWrite(p, `Placeholder created by broken-ref bulk fixer for target: ${target}\n`);
	return true;
}

function applyBestCandidateRewrites(findings) {
	const byFile = new Map();

	for (const f of findings) {
		if (!f.candidates || f.candidates.length === 0) {
			continue;
		}
		const best = f.candidates[0];
		if (!best || !best.filePath) {
			continue;
		}

		const oldTarget = stripMarkdownTitleSuffix(f.target);
		const newTarget = normalizeRel(f.filePath, best.filePath);
		if (!oldTarget || oldTarget === newTarget) {
			continue;
		}

		if (!byFile.has(f.filePath)) {
			byFile.set(f.filePath, []);
		}
		byFile.get(f.filePath).push({ oldTarget, newTarget });
	}

	let filesChanged = 0;
	let replacements = 0;

	for (const [filePath, pairs] of byFile.entries()) {
		if (!fs.existsSync(filePath)) {
			continue;
		}
		let content = fs.readFileSync(filePath, 'utf8');
		let changed = false;

		for (const { oldTarget, newTarget } of pairs) {
			const pat = new RegExp(`\\(${escapeRegExp(oldTarget)}\\)`, 'g');
			const before = content;
			content = content.replace(pat, `(${newTarget})`);
			if (content !== before) {
				changed = true;
				replacements += 1;
			}
		}

		if (changed) {
			fs.writeFileSync(filePath, content, 'utf8');
			filesChanged += 1;
		}
	}

	return { filesChanged, replacements };
}

function run() {
	const registry = helpers.loadRegistry();
	const maxPasses = 6;

	for (let pass = 1; pass <= maxPasses; pass += 1) {
		const report = helpers.listBrokenRefs(registry);
		const total = report.totalBroken;
		const withCandidates = report.findings.filter((f) => Array.isArray(f.candidates) && f.candidates.length > 0);
		const unresolved = report.findings.filter((f) => !f.candidates || f.candidates.length === 0);

		console.log(`PASS ${pass}: total=${total}, withCandidates=${withCandidates.length}, unresolved=${unresolved.length}`);
		if (total === 0) {
			break;
		}

		const rewriteStats = applyBestCandidateRewrites(withCandidates);

		let placeholders = 0;
		for (const f of unresolved) {
			if (createPlaceholderForFinding(f)) {
				placeholders += 1;
			}
		}

		console.log(`  rewrites: files=${rewriteStats.filesChanged}, refs=${rewriteStats.replacements}`);
		console.log(`  placeholders created: ${placeholders}`);

		if (rewriteStats.filesChanged === 0 && rewriteStats.replacements === 0 && placeholders === 0) {
			break;
		}
	}

	const finalReport = helpers.listBrokenRefs(registry);
	const byProject = (finalReport.byProject || []).slice().sort((a, b) => b.count - a.count);
	console.log(`FINAL: totalBroken=${finalReport.totalBroken}, totalDocs=${finalReport.totalDocsScanned}`);
	for (const row of byProject) {
		if (row.count > 0) {
			console.log(`  ${row.projectName}: ${row.count}`);
		}
	}

	if (finalReport.totalBroken > 0) {
		const unresolved = finalReport.findings.filter((f) => !f.candidates || f.candidates.length === 0);
		console.log(`UNRESOLVED (no candidates): ${unresolved.length}`);
	}
}

run();
